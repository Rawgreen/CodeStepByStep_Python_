
percent = int(input("What percentage did you earn? "))

if percent >=90:
    print("You got an A!")
elif 80<=percent <=89:
    print("You got a B!")
elif 70<= percent <=79:
    print("You got a C!")
elif 60<= percent <= 69:
    print("You got a D!")
elif percent < 60:
    print("You got an F!")
